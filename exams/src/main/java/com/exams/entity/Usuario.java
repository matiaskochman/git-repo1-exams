import org.springframework.roo.addon.json.RooJson;
import org.springframework.roo.addon.tostring.RooToString;

@RooJavaBean
@RooToString
@RooJpaActiveRecord
@RooJson
public class Usuario {

    @NotNull
    private String userName;

    @NotNull
    private String password;

    @NotNull
    private String email;
}
